// Comprehensive lessons data with 200+ lessons across all modules

export type LessonType = "reading" | "interactive" | "challenge" | "quiz" | "project"
export type Difficulty = "beginner" | "intermediate" | "advanced"

export interface Lesson {
  id: string
  title: string
  duration: string
  completed: boolean
  type: LessonType
  description: string
  content?: LessonContent
}

export interface LessonContent {
  overview: string
  codeExample?: string
  keyConcepts: string[]
  proTips: string[]
  relatedTopics: string[]
  externalLinks: { title: string; url: string }[]
  interactiveExample?: {
    html: string
    css: string
    description: string
  }
}

export interface LearningModule {
  id: string
  title: string
  description: string
  color: string
  difficulty: Difficulty
  topics: string[]
  lessons: Lesson[]
}

