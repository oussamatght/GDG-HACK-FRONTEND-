// Helper functions
export function getModuleById(id: string): LearningModule | undefined {
  return LEARNING_MODULES.find((m) => m.id === id)
}

export function getLessonById(moduleId: string, lessonId: string): Lesson | undefined {
  const module = getModuleById(moduleId)
  return module?.lessons.find((l) => l.id === lessonId)
}

export function getTotalLessons(): number {
  return LEARNING_MODULES.reduce((acc, m) => acc + m.lessons.length, 0)
}

export function getTotalCompletedLessons(): number {
  return LEARNING_MODULES.reduce((acc, m) => acc + m.lessons.filter((l) => l.completed).length, 0)
}

export function getModuleProgress(moduleId: string): number {
  const module = getModuleById(moduleId)
  if (!module) return 0
  const completed = module.lessons.filter((l) => l.completed).length
  return Math.round((completed / module.lessons.length) * 100)
}
